    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       try{
        String s1=name.getText();
        String s2;
        if(gm.isSelected()){
            s2="Male";
        }
        else
            s2="Female";
        int s3=age.getValue();
        long s4=Long.parseLong(phone.getText());
        String s5=(String)start.getSelectedItem();
        String s6=(String)stop.getSelectedItem();
        String s7=(String)type.getSelectedItem();
        String s8=(String)day.getSelectedItem();
        seats.setText(t);
        String s9=seats.getText();
        dbconnect.input.setString(1, s1);
        dbconnect.input.setString(2, s2);
        dbconnect.input.setInt(3, s3);
        dbconnect.input.setLong(4, s4);
        dbconnect.input.setString(5, s5);
        dbconnect.input.setString(6, s6);
        dbconnect.input.setString(7, s7);
        dbconnect.input.setString(8, s8);
        dbconnect.input.setString(9, s9);
        dbconnect.input.executeUpdate();
        JOptionPane.showMessageDialog(null,"successfully added!!!!");
    }catch(Exception e){JOptionPane.showMessageDialog(null,e);}
    }//GEN-LAST:event_jButton2ActionPerformed

