    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try{
            
            String s=(String)day.getSelectedItem();
           if(type.getSelectedItem().equals("luxury"))
            new types.luxury((String)start.getSelectedItem(),(int)start.getSelectedIndex(),(String)stop.getSelectedItem(),(int)stop.getSelectedIndex(),(String)day.getSelectedItem()).setVisible(true);
        else if(type.getSelectedItem().equals("ac"))
            new types.ac((String)start.getSelectedItem(),(int)start.getSelectedIndex(),(String)stop.getSelectedItem(),(int)stop.getSelectedIndex(),(String)day.getSelectedItem()).setVisible(true);
        else if(type.getSelectedItem().equals("non_ac"))
            new types.non_ac((String)start.getSelectedItem(),(int)start.getSelectedIndex(),(String)stop.getSelectedItem(),(int)stop.getSelectedIndex(),(String)day.getSelectedItem()).setVisible(true);
         
        }catch(Exception e){JOptionPane.showMessageDialog(null,age);}
    }//GEN-LAST:event_jButton3ActionPerformed

