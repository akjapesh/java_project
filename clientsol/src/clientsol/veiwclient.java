    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
       try{
           String gender;
                if(showmale.isSelected())
                    gender="Male";
                else if(showfemale.isSelected())
                    gender="Female";
                else
                    gender="Other";
                String s6="";
            if(showhindi.isSelected())
                s6+="Hindi,";
            if(showenglish.isSelected())
                s6+="English,";
            if(showspanish.isSelected())
                s6+="Spanish,";
            if(showchineese.isSelected())
                s6+="Chineese,";
             if( ! s6.equals("") ){
            s6=s6.substring(0, s6.length()-1);
        }
             java.text.DateFormat dateformat=new java.text.SimpleDateFormat("dd-MMM-yyyy");
             String date=dateformat.format(showdate.getDate());
        dbconnect.upd.setString(1, showname.getText());
        dbconnect.upd.setString(2, gender);
        dbconnect.upd.setString(3, date);
        dbconnect.upd.setString(4, (String)showcountry.getSelectedItem());
        dbconnect.upd.setString(5, showaddress.getText());
        dbconnect.upd.setString(6, s6);
        dbconnect.upd.setInt(7, Integer.parseInt(showcid.getText()));
        dbconnect.upd.executeUpdate();
         searchclientKeyReleased(null);
        JOptionPane.showMessageDialog(null, "Successfully updated");
       }catch(Exception e){JOptionPane.showMessageDialog(null, e);}
    }//GEN-LAST:event_updateActionPerformed

