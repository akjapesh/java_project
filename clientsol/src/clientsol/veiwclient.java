    private void searchclientKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchclientKeyReleased
       try{
        javax.swing.table.DefaultTableModel dtm=(javax.swing.table.DefaultTableModel)table.getModel();
        int rc=dtm.getRowCount();
        while(rc--!=0)
            dtm.removeRow(0);
        String t=searchclient.getText();
        dbconnect.read.setString(1,"%"+t+"%");
        ResultSet rs=dbconnect.read.executeQuery();
        while(rs.next())
        {
            java.util.Vector vec=new java.util.Vector();
            vec.add(rs.getInt("cid"));
            vec.add(rs.getString("name"));
            vec.add(rs.getString("gender"));
            vec.add(rs.getString("dob"));
            vec.add(rs.getString("country"));
            vec.add(rs.getString("address"));
            vec.add(rs.getString("language"));
            dtm.addRow(vec);
        }
       }catch(Exception e){JOptionPane.showMessageDialog(null, e);} 
    }//GEN-LAST:event_searchclientKeyReleased

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
       try{
        int row=table.getSelectedRow();
        if(row!=-1){
        showcid.setText(table.getValueAt(row,0)+"");
        showname.setText((String)table.getValueAt(row,1));
        showaddress.setText((String)table.getValueAt(row,5));
   
        java.util.Date date=new java.text.SimpleDateFormat("dd-MMM-yyyy").parse((String)table.getValueAt(row, 3));
        showdate.setDate(date);
        showcountry.setSelectedItem((String)table.getValueAt(row, 4));
        if(table.getValueAt(row,2).equals("Male")){
            showmale.setSelected(true);
        }
        else if(table.getValueAt(row,2).equals("Female")){
            showfemale.setSelected(true);
        }
        else{
            showother.setSelected(true);
        }
        showhindi.setSelected(false);
        showenglish.setSelected(false);
        showspanish.setSelected(false);
        showchineese.setSelected(false);
        String language=(String)table.getValueAt(row, 6);
        if( !language.equals("") ){
                java.util.StringTokenizer st=
                        new java.util.StringTokenizer(language,",");
                while(st.hasMoreTokens()){
                    String s=st.nextToken();
                    if(s.equals("Hindi"))
                        showhindi.setSelected(true);
                    else if(s.equals("English"))
                        showenglish.setSelected(true);
                    else if(s.equals("Spanish"))
                        showspanish.setSelected(true);
                    else if(s.equals("Chineese"))
                        showchineese.setSelected(true);
                }
            }
        
        }
        }catch(Exception e){JOptionPane.showMessageDialog(null, e);}
    }//GEN-LAST:event_tableMouseClicked

